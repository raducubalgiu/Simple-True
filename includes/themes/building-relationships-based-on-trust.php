        <p>Oamenii sunt fiinţe foarte complexe. Conştiente de multe dintre nevoile lor, sunt foarte desprinse însă de elementele care le determină să aleagă cui să le spună sau în cine să aibă încredere pe drumul către atingerea propriilor obiective. De la mesaje subliminale, la modul în care percepem lumea şi interacţiunile la care luăm parte, toate aceste mici detalii influenţează modul în care ne construim şi ne dezvoltăm relaţiile cu cei din jur. </p>
        <p>Suntem capabili să menţinem contracte şi înţelegeri, să urmăm un drum stabilit, însă valirizăm încrederea mai presus de orice formă de contract. Cum poate fi obţinută, de ce are nevoie, cum poate fi păstrată?</p>
        <p>Şi mai ales, ce putem face zi de zi pentru a dezvolta relaţii de încredere cu cât mai mult persoane? Pentru că acest element stă la baza succesului fiecărei comunităţi a acestei specii. Iar o companie nu este altceva decât o comunitate care are nevoie de încrederea membrilor săi.</p>

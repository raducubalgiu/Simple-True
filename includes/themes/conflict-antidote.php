        <p>Divergenţele sunt de regulă cauzele conflictelor în organizaţii. Pot fi divergenţe la nivelul obiectivelor, intereselor, valorilor sau la nivel de comunicare.</p>
        <p>Pentru a ajunge la o înţelegere mai bună şi implicit la o gestionare eficientă a conflictului la nivel de organizaţie, trebuie să înţelegem mai întâi: comportamentele oamenilor şi factorii care generează starea de conflict, tipul conflictului şi ce putem învâţa din această situaţie. În anii 1970, cercetătorii Kenneth Thomas şi Ralph Kilmann au identificat cinci stiluri generale de abordare a conflictelor, rezultate din combinarea a două dimensiuni fundamentale: preocuparea pentru celălalt sau cooperarea şi preocuparea pentru sine sau asertivitatea. Cele cinci strategii principale de soluţionare a conflictelor sunt: competiţia, colaborarea, compromisul, acomodarea şi evitarea.</p>
    </div>
    <div class="col-md-4">
        <p style="font-weight: 700;">Recomandat pentru cei care vor să:</p>
        <ul>
            <li>genereze un proces de învăţare în urma fiecărui conflict</li>
            <li>medieze mai bine conflictele în care sunt implicaţi ei sau persoanele din jur</li>
            <li>înţeleagă mai bine etapele şi cauzele conflictului</li>
        </ul>
